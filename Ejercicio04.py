#Ejercicio #04 (Hacer un programa que permita ingresar el peso de una persona en kilogramos y devolver dicho peso en libras).

# 1 kg = 2.20462 libras
# Leer el peso en kilogramos
peso_kg = float(input("Por favor, ingrese su peso en kilogramos: "))
peso_libras = peso_kg * 2.20462

print(f"\nSu peso en libras es: {peso_libras} lbs")

