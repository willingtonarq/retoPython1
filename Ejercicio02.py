#Ejercicio #02 (Escribir un programa que calcule el área de un triángulo recibiendo como entrada el valor de base y altura. Área= base*altura/2).

# Leer la base del triángulo
base = float(input("Por favor, ingrese la base del triángulo (en cm): "))
altura = float(input("Por favor, ingrese la altura del triángulo (en cm): "))
area = (base * altura) / 2
print(f"\nEl área del triángulo es: {area} cm²")

