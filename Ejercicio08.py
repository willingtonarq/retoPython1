#Ejercicio #08 (Diseñe un programa que calcule e imprima el número de segundos que hay en un determinado número de días).

# 1 día = 86400 segundos
# Leer el número de días
dias = int(input("Por favor, ingrese el número de días: "))

# Calcular el número de segundos
segundos = dias * 86400
print(f"\nEl número de segundos en {dias} días es: {segundos} segundos")

